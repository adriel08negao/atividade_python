
check_in = input("Como está se sentindo hoje?: ").lower()

if ( check_in == "feliz" or "Animado"):
    print (f"Que bom que está assim! Faça algo que te alegre mais!!")

elif ( check_in == "Cansado"):
    print (f"Um ótimo descanso te ajudaria bastante!")

elif ( check_in == "Estressado"):
    print (f"Pratique algo relaxante")

else:
    print (f"Não sei como te ajudar"  )







