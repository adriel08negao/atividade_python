
salario = int(input("Digite seu salário: "))
tempo = int(input("Digite seu tempo de empresa em anos: "))

if (salario <= 3000 and tempo >= 2):
    print(f"Se seu salário é de: {salario} e você tem {tempo} anos, seu bônus será concedido! ")
    
else:  
    print(f"Se seu salário é de: {salario} e você tem {tempo} anos, seu bônus não será concedido! ")






